package com.meteorology.swat.bean;

public class FrontPageDetailsBean {
	
	private long userCount, observationCount, systemsCount;

	public long getUserCount() {
		return userCount;
	}

	public void setUserCount(long userCount) {
		this.userCount = userCount;
	}

	public long getObservationCount() {
		return observationCount;
	}

	public void setObservationCount(long observationCount) {
		this.observationCount = observationCount;
	}

	public long getSystemsCount() {
		return systemsCount;
	}

	public void setSystemsCount(long systemsCount) {
		this.systemsCount = systemsCount;
	}
	

}
