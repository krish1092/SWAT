package com.meteorology.swat.model;

public class EventsCount {
	
	private int tornadoCount = 0, thunderstormWindCount = 0, hailCount = 0, flashfloodCount = 0;

	public int getTornadoCount() {
		return tornadoCount;
	}

	public void setTornadoCount(int tornadoCount) {
		this.tornadoCount = tornadoCount;
	}

	public int getThunderstormWindCount() {
		return thunderstormWindCount;
	}

	public void setThunderstormWindCount(int thunderstormWindCount) {
		this.thunderstormWindCount = thunderstormWindCount;
	}

	public int getHailCount() {
		return hailCount;
	}

	public void setHailCount(int hailCount) {
		this.hailCount = hailCount;
	}

	public int getFlashfloodCount() {
		return flashfloodCount;
	}

	public void setFlashfloodCount(int flashfloodCount) {
		this.flashfloodCount = flashfloodCount;
	}
	

}
